def get_text(lang: str, key: str, **kwargs: str) -> str:
    """–ü–æ–ª—É—á–∏—Ç—å —Ç–µ–∫—Å—Ç –Ω–∞ –Ω—É–∂–Ω–æ–º —è–∑—ã–∫–µ —Å —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ–º
    
    Args:
        lang: –ö–æ–¥ —è–∑—ã–∫–∞ ('ru' –∏–ª–∏ 'uz')
        key: –ö–ª—é—á —Ç–µ–∫—Å—Ç–∞ –∏–∑ TEXTS
        **kwargs: –ü–∞—Ä–∞–º–µ—Ç—Ä—ã –¥–ª—è —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è —Å—Ç—Ä–æ–∫–∏
        
    Returns:
        –û—Ç—Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–Ω–∞—è —Å—Ç—Ä–æ–∫–∞ —Ç–µ–∫—Å—Ç–∞ –∏–ª–∏ —Å–∞–º –∫–ª—é—á, –µ—Å–ª–∏ —Ç–µ–∫—Å—Ç –Ω–µ –Ω–∞–π–¥–µ–Ω
    """
    try:
        texts = TEXTS.get(lang, TEXTS.get('ru', {}))
        text = texts.get(key, key)
        
        # –ï—Å–ª–∏ —Ç–µ–∫—Å—Ç –Ω–µ –Ω–∞–π–¥–µ–Ω, –ø—Ä–æ–±—É–µ–º —Ä—É—Å—Å–∫–∏–π
        if text == key and lang != 'ru':
            text = TEXTS.get('ru', {}).get(key, key)
        
        # –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ–º, –µ—Å–ª–∏ –µ—Å—Ç—å –ø–∞—Ä–∞–º–µ—Ç—Ä—ã –∏ —Ç–µ–∫—Å—Ç —Å–æ–¥–µ—Ä–∂–∏—Ç –ø–ª–µ–π—Å—Ö–æ–ª–¥–µ—Ä—ã
        if kwargs and text != key:
            try:
                return text.format(**kwargs)
            except (KeyError, ValueError) as e:
                # –ï—Å–ª–∏ —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ –Ω–µ —É–¥–∞–ª–æ—Å—å, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º —Ç–µ–∫—Å—Ç –±–µ–∑ —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è
                import logging
                logging.warning(f"Format error in get_text: {e}, key={key}, lang={lang}")
                return text
        
        return text
    except Exception as e:
        import logging
        logging.error(f"Error in get_text: {e}, key={key}, lang={lang}")
        return key

